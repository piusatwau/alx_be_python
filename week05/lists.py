fruits = ["mangoes", "oranges", "Apples", "Banana"]
print(fruits)

# list methods

# removing last item using pop

fruits.pop()
print(fruits)

# removing an item in the list by its index using pop()

fruits.pop(2)
print(fruits)

# using remove method to remove any item in the list by its value
fruits.remove("mangoes")
print(fruits)
